import SwiftUI

extension Color {

    static let brandDark        = Color("brandDark")
    static let brandLight       = Color("brandLight")
    static let brandPrimary     = Color("brandPrimary")
    static let brandInverse     = Color("brandInverse")
    static let brandSecondary   = Color("brandSecondary")

}

extension UIColor {

    static let brandDark        = UIColor(named: "brandDark")
    static let brandLight       = UIColor(named: "brandLight")
    static let brandPrimary     = UIColor(named: "brandPrimary")
    static let brandSecondary   = UIColor(named: "brandSecondary")

}
